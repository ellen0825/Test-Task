import React, { useState } from 'react'
import Head from 'next/head'
import Header from '../components/layout/Header'
import Main from '../components/landing/Main'

import { getTariffs } from '../lib/tariffs'

export async function getServerSideProps() {
  let tariffs = []
  try {
    tariffs = await getTariffs()
  } catch (e) {
    console.error('Failed to fetch tariffs:', e)
  }
  return { props: { tariffs } }
}

export default function Home({ tariffs }) {
  const [timerEnded, setTimerEnded] = useState(false)
  return (
    <div>
      <Head>
        <title>Test</title>
        <meta name="description" content="Generated by Masha" />
        <link rel="icon" href="/logo.svg" />
      </Head>
      <Header onTimerEnd={() => setTimerEnded(true)} />
      <Main tariffs={tariffs} timerEnded={timerEnded} />
    </div>
  )
}
